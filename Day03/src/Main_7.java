
public class Main_7 {

	public static void main(String[] args) {

		System.out.println("-----------------------------------------------------------");

		System.out.println("100미만의 수중 짝수 출력");

		for (int i = 2; i < 100; i = i + 2) {

			System.out.println(i);
		}
		System.out.println("-----------------------------------------------------------");
		System.out.println("100미만의 수중 홀수 출력");

		for (int i = 1; i < 100; i = i + 2) {

			System.out.println(i);
		}

		System.out.println("-----------------------------------------------------------");

	}
}
